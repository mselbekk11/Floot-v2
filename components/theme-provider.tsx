// components/theme-provider.tsx
'use client';

import { ThemeProvider as NextThemesProvider } from 'next-themes';

export function ThemeProvider({ children }: { children: React.ReactNode }) {
  return (
    <NextThemesProvider
      attribute='class'
      forcedTheme='light'
      disableTransitionOnChange
    >
      {children}
    </NextThemesProvider>
  );
}
